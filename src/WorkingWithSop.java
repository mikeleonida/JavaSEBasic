
public class WorkingWithSop {
	public static void main(String[] args) {
		int no = 10;
		int nextNum = 20;
		
		System.out.println("no + nextNum = " + (no + nextNum));
		System.out.println(no + " + " + nextNum + " = " + no + nextNum);
		System.out.println(no + nextNum + " = " + no + nextNum);
	}
}
